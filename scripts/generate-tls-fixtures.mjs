#!/usr/bin/env node
import { mkdirSync, rmSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { spawnSync } from 'node:child_process';

const outDir = join(process.cwd(), 'test-fixtures', 'tls', 'generated');
const password = 'poo-pi-test-password';

function run(command, args) {
  const result = spawnSync(command, args, { stdio: 'inherit' });
  if (result.status !== 0) process.exit(result.status ?? 1);
}

rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

run('openssl', ['genrsa', '-out', join(outDir, 'ca.key'), '2048']);
run('openssl', ['req', '-x509', '-new', '-nodes', '-key', join(outDir, 'ca.key'), '-sha256', '-days', '30', '-subj', '/CN=poo-pi-test-ca', '-out', join(outDir, 'ca.crt')]);
run('openssl', ['genrsa', '-out', join(outDir, 'client.key'), '2048']);
run('openssl', ['req', '-new', '-key', join(outDir, 'client.key'), '-subj', '/CN=poo-pi-test-client', '-out', join(outDir, 'client.csr')]);
run('openssl', ['x509', '-req', '-in', join(outDir, 'client.csr'), '-CA', join(outDir, 'ca.crt'), '-CAkey', join(outDir, 'ca.key'), '-CAcreateserial', '-out', join(outDir, 'client.crt'), '-days', '30', '-sha256']);
run('openssl', ['pkcs12', '-export', '-out', join(outDir, 'client.p12'), '-inkey', join(outDir, 'client.key'), '-in', join(outDir, 'client.crt'), '-certfile', join(outDir, 'ca.crt'), '-passout', `pass:${password}`, '-keypbe', 'AES-256-CBC', '-certpbe', 'AES-256-CBC']);

writeFileSync(join(outDir, 'README.md'), `# Generated TLS fixtures\n\nThese files are local test-only artifacts generated by \`npm run fixtures:tls\`.\n\nKnown PFX password: \`${password}\`\n\nDo not use these certificates for production.\n`);
console.log(`generated TLS fixtures in ${outDir}`);
